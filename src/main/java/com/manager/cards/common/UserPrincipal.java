package com.manager.cards.common;

import com.manager.cards.config.SecurityConfig;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

/**
 * This class provides core user information and stores user information which can be encapsulated into
 * Authentication objects. This allows non-security related user information (such as email addresses,
 * numbers etc) to be stored in a convenient location.
 */
public class UserPrincipal implements UserDetails {

    private User user;

    public UserPrincipal(User user) {
        this.user = user;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        List<GrantedAuthority> list = new ArrayList<>();
        list.add(new SimpleGrantedAuthority(SecurityConfig.ROLE_PREFIX + SecurityConfig.USER_ROLE));
        if (getUsername().equals(SecurityConfig.ADMIN_USERNAME)) {
            list.add(new SimpleGrantedAuthority(SecurityConfig.ROLE_PREFIX + SecurityConfig.ADMIN_ROLE));
        }
        return list;
    }

    @Override
    public String getPassword() {
        return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getUsername();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
